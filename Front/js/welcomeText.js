export function getModeIntroMessage(mode) {
    switch (mode) {

        case "full":
            return `
📝 **자기소개서를 처음부터 함께 써볼게요!**  
글쓰기가 어렵게 느껴지더라도 괜찮아요.  
제가 하나씩 도와드릴 테니까 부담 없이 시작해요 😊 

---

**먼저, 아래 질문들 중 하나를 골라 답해주셔도 좋아요:**
            
✅ 어떤 직무에 지원하고 계신가요?  
✅ 나의 강점은 뭐라고 생각하나요?  
✅ 특별한 경험이 있었나요?  
✅ 입사 후 어떤 목표가 있으신가요?
            
함께 차근차근 문장으로 만들어봐요! ✍️`
        
        case "edit":
            return `
✂️ **입력하신 문장을 더 자연스럽고 설득력 있게 다듬어드릴게요!**  
문장을 붙여주시면, 어색한 표현을 고쳐드리고  
더 멋진 문장으로 바꿔드릴게요 💬  

--- 
✅ 예를 들어 이런 것도 좋아요:

**저는 항상 책임감을 가지고 일합니다.**

👇 이렇게 바꿔드릴 수도 있어요:

**저는 어떤 일이든 끝까지 책임지고 완수하는 태도를 가지고 있습니다.**

한 문장부터 여러 문장까지 모두 환영이에요! 😄`;
        
        case "idea":
            return `
💡 **무슨 내용을 쓸지 막막하신가요?**  
괜찮아요! 저랑 함께 **브레인스토밍** 해봐요 🔍

---

**먼저 간단히 알려주세요!:**

✅ 기억에 남는 경험이 있나요?
✅ 어떤 가치를 중요하게 생각하시나요?
✅ 나를 잘 표현하는 키워드는 뭘까요?

조금씩 떠올려드릴 수 있게 질문해드릴게요.  
편하게 이야기해 주세요 😊`;

        case "custom":
            return `
🏢 **기업에 꼭 맞는 자기소개서를 함께 만들어볼게요!**  
지원하는 **회사나 직무에 어울리는 문장**으로 수정해드릴게요 😊

---

**✍️ 아래 두 가지만 알려주시면 시작할 수 있어요!**

1. ✅ **지원하는 기업의 인재상이나 가치관**  
   예: "창의성과 도전정신을 중시해요" / "팀워크와 소통을 중요하게 생각해요"

2. ✏️ **수정하고 싶은 자기소개서 문장**  
   예: "저는 항상 새로운 도전을 즐깁니다."

---

**두 가지 정보를 알려주시면, 기업에 맞게 더 멋지고 설득력 있는 문장으로 바꿔드릴게요! 🔧**  
질문이 있다면 언제든 편하게 물어보세요 😄`;

        default:
            return "안녕하세요! 무엇을 도와드릴까요? 😊";
    }
}